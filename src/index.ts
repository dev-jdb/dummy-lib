/**
 * @description
 * @param {number} x
 * @param {number} y
 * @returns {number}
 * @example
 * add(1, 2) // 3
 * add(20, 2) // 22
 * */
export const add = (x: number, y: number): number => x + y;
/**
 * @description
 * @param {number} x
 * @param {number} y
 * @returns {number}
 * @example
 * subtract(1, 2) // -1
 * subtract(20, 2) // 18
 * */
export const subtract = (x: number, y: number): number => x - y;
